    private class MouseHandler extends MouseAdapter {

        @Override
        public void mouseReleased(MouseEvent e) {

            if (sekil!=null) {
                if (ilkNokta) {
                    p1 = e.getPoint();

                    jLabel1.setText("İkinci noktayı seçiniz");
                    ilkNokta = false;
                    if (!sekil.ikiNoktali()) {
                        drawShape();
                    }

                } else {
                    p2 = e.getPoint();
                    jLabel1.setText("ilk=" + p1.x + "," + p1.y + "/" + "ikinci="
                            + p2.x + "," + p2.y);

                    ilkNokta = true;
                    drawShape();
                }
            }

        }

    }

